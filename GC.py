# GRADE CALCULATOR  

m = int(input("Enter your marks: "))

if m >= 90:
    print("Congrates, your grade is A...")

elif m >= 80:
    print("Your grade is B.")

elif m >= 70:
    print("Your grage is C.")

elif m >= 60:
    print("Your grade is D.")

elif m >= 40:
    print("Your grade is E.")

else:
    print("Your grade is F. Better luck next time.")
